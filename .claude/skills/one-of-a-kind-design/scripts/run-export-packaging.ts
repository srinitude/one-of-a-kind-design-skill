function formatBytes(bytes: number): string {
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
}

// --- Archive Creation ---

export function createZipArchive(
  input: ExportInput,
  files: Array<{ path: string; relativePath: string; size: number }>,
): Effect.Effect<ExportResult, Error> {
  const toolConfig = TOOL_CONFIGS[input.tool];
  const totalSize = files.reduce((sum, f) => sum + f.size, 0);

  return Effect.tryPromise({
    try: async () => {
      // Ensure output directory exists
      const outputDir = input.output.substring(0, input.output.lastIndexOf("/"));
      if (outputDir) {
        try {
          await mkdir(outputDir, { recursive: true });
        } catch {
          /* directory may exist */
        }
      }

      const output = createWriteStream(input.output);
      const archive = archiver("zip", { zlib: { level: 9 } });

      const done = new Promise<void>((resolve, reject) => {
        output.addListener("close", resolve);
        archive.addListener("error", reject);
      });

      archive.pipe(output);

      // Add asset files
      const acceptedExts = new Set(toolConfig.accepts.map((a) => `.${a}`));
      let fileCount = 0;

      for (const file of files) {
        const ext = extname(file.relativePath).toLowerCase();
        if (acceptedExts.has(ext) || acceptedExts.has("*")) {
          archive.file(file.path, { name: `assets/${file.relativePath}` });
          fileCount++;
        }
      }

      // Add generated documents
      const readme = generateReadme(input.tool, fileCount, totalSize);
      archive.append(readme, { name: "README.md" });
      fileCount++;

      const qualityReport = generateQualityReport();
      archive.append(qualityReport, { name: "QUALITY-REPORT.md" });
      fileCount++;

      const styleSpec = generateStyleSpec();
      archive.append(styleSpec, { name: "STYLE-SPEC.md" });
      fileCount++;

      // Add empty directories for structure
      archive.append("", { name: "prompts/.gitkeep" });
      archive.append("", { name: "config/.gitkeep" });
      archive.append("", { name: "guides/.gitkeep" });
      fileCount += 3;

      await archive.finalize();
      await done;

      const zipStat = await stat(input.output);
      return {
        zip_path: input.output,
        file_count: fileCount,
        total_size: zipStat.size,
      } satisfies ExportResult;
    },
    catch: (e) => new Error(`Export packaging failed: ${e}`),
  });
}

// --- CLI Entry ---

const program = Effect.gen(function* () {
  const args = process.argv.slice(2);
  const getArg = (flag: string): string | undefined => {
    const idx = args.indexOf(flag);
    return idx >= 0 ? args[idx + 1] : undefined;
  };

  const tool = (getArg("--tool") ?? "generic") as ExportTool;
  const assetsDir = getArg("--assets-dir");
  const output = getArg("--output") ?? `export-${tool}-${Date.now()}.zip`;

  if (!assetsDir) return yield* Effect.fail(new Error("--assets-dir is required"));

  const validTools = Object.keys(TOOL_CONFIGS) as ExportTool[];
  if (!validTools.includes(tool)) {
    return yield* Effect.fail(new Error(`Invalid tool: ${tool}. Valid: ${validTools.join(", ")}`));
  }

  const noEntries = (): never[] => [];
  const noStat = (): null => null;
  const files = yield* Effect.tryPromise({
    try: async () => {
      const collected: Array<{ path: string; relativePath: string; size: number }> = [];
      const walkDir = async (currentDir: string, prefix: string): Promise<void> => {
        const entries = await readdir(currentDir, { withFileTypes: true }).catch(noEntries);
        for (const entry of entries) {
          const fullPath = join(currentDir, entry.name);
          if (entry.isDirectory()) {
            await walkDir(fullPath, join(prefix, entry.name));
          } else {
            const fileStat = await stat(fullPath).catch(noStat);
            if (fileStat) {
              collected.push({
                path: fullPath,
                relativePath: join(prefix, entry.name),
                size: fileStat.size,
              });
            }
          }
        }
      };
      await walkDir(assetsDir, "");
      return collected;
    },
    catch: (e) => new Error(`Failed to scan assets directory: ${e}`),
  });

  if (files.length === 0) {
    return yield* Effect.fail(new Error(`No files found in assets directory: ${assetsDir}`));
  }

  const result = yield* createZipArchive({ tool, assetsDir, output }, files);

  yield* Console.log(JSON.stringify(result, null, 2));
});

if (import.meta.main) {
  pipe(
    program,
    Effect.catchAll((error) =>
      Effect.sync(() => {
        console.error(`Export packaging failed: ${error}`);
        process.exit(1);
      }),
    ),
    Effect.runPromise,
  );
}
