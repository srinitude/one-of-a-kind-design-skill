export function generateReadme(tool: ExportTool, fileCount: number, totalSize: number): string {
  const config = TOOL_CONFIGS[tool];
  return [
    `# ${config.readme_header}`,
    ``,
    `Generated by One-of-a-Kind Design on ${new Date().toISOString().split("T")[0]}`,
    ``,
    `## Contents`,
    ``,
    `- **assets/**: ${fileCount} design assets (${formatBytes(totalSize)})`,
    `- **config/**: Tool-specific configuration files`,
    `- **prompts/**: Generation prompts used for each asset`,
    `- **guides/**: Implementation guides and best practices`,
    `- **QUALITY-REPORT.md**: Automated quality scores`,
    `- **STYLE-SPEC.md**: Design system specification`,
    ``,
    `## Import Instructions`,
    ``,
    ...getImportInstructions(tool),
    ``,
    `## Accepted Formats`,
    ``,
    config.accepts.map((fmt) => `- .${fmt}`).join("\n"),
    ``,
    `## Quality Guarantee`,
    ``,
    `All assets passed the One-of-a-Kind quality gate (minimum 7.0/10 composite score).`,
    `See QUALITY-REPORT.md for detailed per-asset scoring.`,
  ].join("\n");
}

