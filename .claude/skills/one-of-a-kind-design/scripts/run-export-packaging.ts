/**
 * run-export-packaging.ts — Compress assets and generate tool-specific export packages.
 * Creates .zip archives with README, quality reports, style specs, prompts, assets, configs, and guides.
 *
 * Run: bun run scripts/run-export-packaging.ts --tool "figma" --assets-dir "..." --output "export.zip"
 */

import { createWriteStream } from "node:fs";
import { mkdir, readdir, stat } from "node:fs/promises";
import { extname, join } from "node:path";
import archiver from "archiver";
import { Console, Effect, pipe } from "effect";

// --- Types ---

